##2.(ii)Take two numbers from user and perform arithmetic operations on them.

firstNumber = input('Please enter an integer: ')#####inputting 1st number from user
secondNumber = input('Please enter a second integer: ')#####inputting 2nd number from user

num1 = int(firstNumber)###converting values to integer
num2 = int(secondNumber)###converting values to integer



print('Here is some output')###printing output

print(num1, ' + ', num2, ' = ', num1+num2)####addition operation
print(num1,' - ',num2, ' = ',num1-num2)####subtraction operation
print(num1,' * ',num2, ' = ',num1*num2)####multiplication operation
print(num1,' / ',num2, ' = ',num1/num2)####division operation
